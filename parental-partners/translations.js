// Global function to apply selected language translations
function applyLanguage(lang) {
  if (!translations[lang]) return;
  
  document.querySelectorAll('[data-i18n]').forEach(elem => {
    const key = elem.getAttribute('data-i18n');
    if (translations[lang][key] !== undefined) {
      if (elem.tagName === 'INPUT' || elem.tagName === 'TEXTAREA') {
        elem.placeholder = translations[lang][key].replace(/<[^>]*>/g, ''); // strip HTML tags for placeholders
      } else if (elem.tagName === 'SELECT') {
        // Option translation handling: we can target select placeholders or options
        elem.placeholder = translations[lang][key];
      } else {
        elem.innerHTML = translations[lang][key];
      }
    }
  });

  // Keep specific promo bold codes updated if they exists
  const promoBoldEl = document.getElementById('promo-bold-code');
  if (promoBoldEl) {
    const promoInput = document.getElementById('promoCodeInput');
    if (promoInput) {
      promoBoldEl.innerText = promoInput.value || "JOHN50";
    }
  }
  
  // Highlight active language button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    if (btn.getAttribute('data-lang') === lang) {
      btn.classList.add('active');
    } else {
      btn.classList.remove('active');
    }
  });

  document.documentElement.lang = lang;
  
  // Trigger calculator update if we are on index.html and updateCalcFromInput exists
  if (typeof updateCalcFromInput === 'function') {
    // If the labels have translated, we re-run calculation to update calculations
    updateCalcFromInput();
  }
}

// Global function to set language manually
function setLanguage(lang) {
  localStorage.setItem('pref_lang', lang);
  applyLanguage(lang);
}

// Detect language automatically based on IP or fallback to browser language
async function detectLanguageByIP() {
  // If user already set a preference manually, use it
  let lang = localStorage.getItem('pref_lang');
  if (lang) {
    applyLanguage(lang);
    showBody();
    return;
  }

  // Hide page initially to avoid FOUT
  document.documentElement.style.visibility = 'hidden';

  try {
    const response = await fetch('https://ipapi.co/json/');
    if (response.ok) {
      const data = await response.json();
      if (data && data.country_code === 'TR') {
        lang = 'tr';
      } else {
        lang = 'en';
      }
    }
  } catch (error) {
    console.error("IP Geolocation failed. Checking browser locale...", error);
  }

  // Fallback to browser language if IP geolocation failed or wasn't TR
  if (!lang) {
    const browserLang = (navigator.language || navigator.userLanguage || '').toLowerCase();
    if (browserLang.startsWith('tr')) {
      lang = 'tr';
    } else {
      lang = 'en';
    }
  }

  localStorage.setItem('pref_lang', lang);
  applyLanguage(lang);
  showBody();
}

function showBody() {
  document.documentElement.style.visibility = 'visible';
}

// Run language detection on load
document.addEventListener('DOMContentLoaded', () => {
  detectLanguageByIP();
});
